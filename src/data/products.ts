// Daha gerçekçi karbon ayak izi hesaplama fonksiyonu
function computeFootprint(p: any) {
  const production = p.base_production_emission as number;

  const transportFactors: Record<string, number> = {
    ship: 0.015 / 1000,
    truck: 0.08 / 1000,
    air: 0.6 / 1000,
    plane: 0.6 / 1000,
  };

  const modeFactor =
    transportFactors[p.transport_mode as keyof typeof transportFactors] ??
    transportFactors.truck;

  const shipping =
    (p.shipping_distance_km as number) *
    (p.weight_kg as number) *
    modeFactor;

  const packaging = (p.packaging_factor as number) * 1.5;

  const total = production + shipping + packaging;

  // --- SKOR HESABI (SENİN GÖNDERDİĞİN TABLOYA %100 UYAR) ---
  let score = 1;

  if (total <= 2) score = 1;
  else if (total <= 4) score = 2;
  else if (total <= 5) score = 3;
  else if (total <= 6) score = 4;
  else if (total <= 9) score = 5;
  else if (total <= 25) score = 6;
  else if (total <= 45) score = 7;
  else if (total <= 80) score = 8;
  else if (total <= 100) score = 9;
  else score = 10;

  let label: "Low" | "Medium" | "High";
  if (score <= 3) label = "Low";
  else if (score <= 7) label = "Medium";
  else label = "High";

  return {
    score,
    label,
    estimated_kgCO2e: total,
    breakdown: { production, shipping, packaging, weight: 0 },
  };
}

